    private void jBtnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAdicionarActionPerformed
        if ((jTxtNomeCliente.getText().isEmpty()) || (jTxtDocumento.getText().isEmpty())
            || (jTxtNomeCliente.getText().isBlank()) || (jComboxEstado.getSelectedItem().equals(" ")) || (jTxtEmail.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!!!");
        } else {

            String nome = jTxtNomeCliente.getText();
            String documento = jTxtDocumento.getText();
            String tipo_Cliente;
            if(jRdbtonFisica.isSelected()){
                tipo_Cliente=jRdbtonFisica.getText();
            }else{
                tipo_Cliente=jRdBtnJuridica.getText();
            }              
            String telefone = jTxtTelefone.getText();
            String email = jTxtEmail.getText();
            String logradouro = jTxtLogradouro.getText();
            String numero = jTxtNumero.getText();
            String complemento = jTxtComplemento.getText();
            String bairro = jTxtBairro.getText();
            String cidade = jTxtCidade.getText();
            String estado = jComboxEstado.getSelectedItem().toString();
            String cep = jTxtCEP.getText();
            String observacoes = jTxtArea.getText();
            Cliente cliente = new Cliente(nome,  documento,  tipo_Cliente,  telefone,  email,  logradouro,  numero,
             complemento,  bairro,  cidade,  estado,  cep,  observacoes);

            ClienteDAO dao = new ClienteDAO();
            dao.adicionarCliente(cliente);
            
            jTxtNomeCliente.setText(null);
            jTxtDocumento.setText(null);
            jTxtEmail.setText(null);
            jTxtTelefone.setText(null);
            jTxtCEP.setText(null);
            jTxtLogradouro.setText(null);
            jTxtComplemento.setText(null);
            jTxtBairro.setText(null);
            jTxtNumero.setText(null);
            jTxtObservacoes.setText(null);
            jBtnAdicionar.setEnabled(true);
            jBtnEditar.setEnabled(false);
            jBtnExcluir.setEnabled(false);
       }
        listar();
    }//GEN-LAST:event_jBtnAdicionarActionPerformed

