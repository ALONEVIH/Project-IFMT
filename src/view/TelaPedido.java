    private void jBtnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAdicionarActionPerformed
        if ((jTxtID.getText().isEmpty()) || (jTxtValorTotal.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Cliente não selecionado ou valor do pedido vazio!!!\nPreencha os campos");
        } else {

           
            String nome_Cliente = jTxtClientePesquisar.getText();
            int id_Cliente = Integer.parseInt(jTxtID.getText());
            String status = jCboPedidoSituacao.getSelectedItem().toString();
            double valor = Double.parseDouble(jTxtValorTotal.getText());
            String observacoes = jTxtObservacoes.getText();

            Pedido pedido = new Pedido(nome_Cliente, id_Cliente, status, valor, observacoes);
            PedidoDAO dao = new PedidoDAO();
            dao.adicionarPedido(pedido);
            
            jTxtClientePesquisar.setText(null);
            jTxtValorTotal.setText(null);
            jTxtID.setText(null);
            jTxtObservacoes.setText(null);
            jBtnAdicionar.setEnabled(true);
            jBtnPesquisar.setEnabled(true);
            jBtnEditar.setEnabled(false);
            jBtnExcluir.setEnabled(false);
            jBtnImprimir.setEnabled(false);
        }
        listar();
    }//GEN-LAST:event_jBtnAdicionarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        if ((jTxtPedido.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Pedido não selecionado ou vazio!!!\nPreencha os campos");
        }else{
        try{
            
            
            int id = Integer.parseInt(jTxtNumeroPedido.getText());
            String nome_Cliente = jTxtClientePesquisar.getText();
            int id_Cliente = Integer.parseInt(jTxtID.getText());
            String status = jCboPedidoSituacao.getSelectedItem().toString();
            double valor = Double.parseDouble(jTxtValorTotal.getText());
            String observacoes = jTxtObservacoes.getText();
            
            Pedido pedido = new Pedido(id, id_Cliente, nome_Cliente, status, valor, observacoes);
            PedidoDAO dao = new PedidoDAO();
            dao.excluirPedido(pedido);
            
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Erro: Verifique os valores numéricos!");
            } catch (SQLException e) { // ← Adicione esta linha
                JOptionPane.showMessageDialog(null, "Erro no banco de dados: " + e.getMessage());
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao excluir pedido: " + e.getMessage());
            }
        }
        listar();
         jTxtNumeroPedido.setText(null);
        jTxtData.setText(null);
        jCboPedidoSituacao.setSelectedIndex(0);
        jTxtClientePesquisar.setText(null);
        jTxtID.setText(null);
        jTxtValorTotal.setText(null);
        jTxtObservacoes.setText(null);
        jTxtPedido.setText(null);
       
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
        if ((jTxtValorTotal.getText().isEmpty())||jTxtID.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!!!\nPreencha os campos");
        }else{    
            int id = Integer.parseInt(jTxtNumeroPedido.getText());
            String status = jCboPedidoSituacao.getSelectedItem().toString();
            double valor = Double.parseDouble(jTxtValorTotal.getText());
            String observacoes = jTxtObservacoes.getText();
    
            Pedido pedido = new Pedido(status, valor, observacoes, id);
           
            PedidoDAO dao = new PedidoDAO();
            dao.alterarPedido(pedido);
        }
            jTxtNumeroPedido.setText(null);
            jTxtData.setText(null);
            jCboPedidoSituacao.setSelectedIndex(0);
            jTxtClientePesquisar.setText(null);
            jTxtID.setText(null);
            jTxtValorTotal.setText(null);
            jTxtObservacoes.setText(null);
            jTxtPedido.setText(null);
            jBtnAdicionar.setEnabled(true);
            jBtnEditar.setEnabled(false);
            jBtnExcluir.setEnabled(false);
            jBtnImprimir.setEnabled(false);
            listar();

    }//GEN-LAST:event_jBtnEditarActionPerformed

